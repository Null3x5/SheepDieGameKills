
public class Node {
	private int x;
	private int y;
	public Node(int x, int y){
		setXCord(x);
		setYCord(y);
	}
	private void setXCord(int x){
		this.x = x;
	}
	private void setYCord(int y){
		this.y = y;
	}
	public int getX(){
		return x;
		
	}
	public int getY(){
		return y;
		
	}

}
